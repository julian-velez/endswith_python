# Se crea una variable llamada 'parrafo' que contiene una cadena de texto.
parrafo = 'hola julian velez G.'

# Se utiliza el método 'endswith' para verificar si la cadena 'parrafo' termina con un punto (".")
# Este método devuelve True si la cadena termina con ese carácter, de lo contrario devuelve False.
final = parrafo.endswith(".")

# Se imprime el resultado de la verificación anterior (True o False).
print(final)
